function get_cosine_similarity(vector1, vector2) {
  // calculate the dot product of the two vectors
  let dot_product = 0;
  for (let i = 0; i < vector1.length; i++) {
    dot_product += vector1[i] * vector2[i];
  }
  
  // calculate the magnitude of the two vectors
  let magnitude1 = 0;
  let magnitude2 = 0;
  for (let i = 0; i < vector1.length; i++) {
    magnitude1 += vector1[i] * vector1[i];
    magnitude2 += vector2[i] * vector2[i];
  }
  magnitude1 = Math.sqrt(magnitude1);
  magnitude2 = Math.sqrt(magnitude2);
  
  // calculate the cosine similarity
  const cosine_similarity = dot_product / (magnitude1 * magnitude2);
  return cosine_similarity;
};

export function similarity_all(cur_line_vector) {
  let similarities = [];
  for (let i = 0; i < average_vectors.length; i++) {
    similarities.push(get_cosine_similarity(cur_line_vector, average_vectors[i].vector));
  }
  // return similarity score and name of the vector
  return similarities.map((similarity, index) => ({
    similarity,
    name: average_vectors[index].name
  }));
}
